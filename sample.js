// Sample : 1
let num1 = 5;
let num2 = 10;
let num3 = 15;
console.log(num1 + num2 + num3); // 30
// Explanation: The numbers 5, 10, and 15 are added together to get 30.

// Sample : 2
let str1 = "Good";
let str2 = "Morning";
console.log(str1 + " " + str2); // "Good Morning"
// Explanation: The strings "Good" and "Morning" are concatenated with a space in between to form "Good Morning".

// Sample : 3
let bool1 = false;
let bool2 = true;
console.log(bool1 || bool2); // true
// Explanation: The logical OR (||) operator returns true because one of the operands (bool2) is true.
